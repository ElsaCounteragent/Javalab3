//     Последовательность закольцована, 
//     т. е. если при очередном поиске исключаемой кости дошли до конца последовательности, то продолжить поиск необходимо от начала последовательности. 
//     Используйте для хранения набора и последовательности костей только один массив из 28 элементов. 
