//    перемешивает набор и вновь выводит на экран.
//     Далее полученный после перемешивания набор становится последовательностью. 
//     По заданному числу N (задается в main()) последовательно исключить сначала кость N от начала последовательности, 
//     потом кость N от следующей после исключенной и так далее пока из последовательности не будут исключены все кости. 
