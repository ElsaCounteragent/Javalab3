//Назначение: присваивание N, создание коллекции, вывод коллекции, перемешивание коллекции, вывод коллекции, экстракция элементов последовательности
public class App {
    public static void main(String[] args){
        final int N = 2; //N - по условию задачи

        Collection collection = new Collection(); //создаём коллекцию
        collection.print();
        System.out.println("\nShuffling...");
        collection.shuffle();
        collection.print();

        System.out.println("\nExtracting...\n");

        Sequence sequence = new Sequence(collection);
        //создаём последовательность
        while(!sequence.isEmpty()){
            //пока последовательность не пуста проводим экстракцию
            sequence.extractNext(N).print();
            System.out.println();
            System.out.println();
            sequence.print();
            System.out.println();
            System.out.println("--------------------");
        }
    }
}